            "ReadOnlyListSlim<>" => $"Core.Collections.ReadOnlyListSlim<{@params[0]}>",
            "ReadOnlyMemory<>" => $"System.ReadOnlyMemory<{@params[0]}>",
            "ReadOnlySequence<>" => $"System.Buffers.ReadOnlySequence<{@params[0]}>",
