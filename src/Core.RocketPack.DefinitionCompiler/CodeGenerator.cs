            "BytesOperations" => "Core.Base.BytesOperations",
            "CancellationToken" => $"System.Threading.CancellationToken",
            "CollectionHelper" => "Core.Base.Helpers.CollectionHelper",
            "Dictionary<,>" => $"System.Collections.Generic.Dictionary<{@params[0]}, {@params[1]}>",
            "FormatException" => "System.FormatException",
            "HashCode" => "System.HashCode",
            "IBufferWriter<>" => $"System.Buffers.IBufferWriter<{@params[0]}>",
            "IBytesPool" => "Core.Base.IBytesPool",
            "IConnection" => $"Core.Net.Connections.IConnection",
            "IDisposable" => "System.IDisposable",
            "IMemoryOwner<>" => $"System.Buffers.IMemoryOwner<{@params[0]}>",
            "IRocketMessage" => $"Core.RocketPack.IRocketMessage<{@params[0]}>",
            "IRocketMessage<>" => $"Core.RocketPack.IRocketMessage<{@params[0]}>",
            "IRocketMessageFormatter<>" => $"Core.RocketPack.IRocketMessageFormatter<{@params[0]}>",
            "IRocketRemotingCallerFactory" => $"Core.RocketPack.Remoting.IRocketRemotingCallerFactory<{@params[0]}>",
            "IRocketRemotingListenerFactory" => $"Core.RocketPack.Remoting.IRocketRemotingListenerFactory<{@params[0]}>",
            "Lazy<>" => $"System.Lazy<{@params[0]}>",
            "Memory<>" => $"System.Memory<{@params[0]}>",
            "MemoryOwner<>" => $"Core.Base.MemoryOwner<{@params[0]}>",
            "ObjectHelper" => "Core.Base.Helpers.ObjectHelper",
